function [theta] = eval_quad(Ix,Iy,It,u,v)
%THIS FUNCTION TAKES IX,IY AND IT AS IMAGE DERIVATIVES, U AND
%V AS OPTICAL FLOW COMPONENT. THIS FUNCTION 
%CALCULATES THE QUADRATIC NORM AND OUTPUTS AN ARRAY OF THE
% SAME SIZE AS THAT OF IX (AS WELL AS THAT OF IY, IT, U AND V).

theta = (u.*Ix + v.*Iy + It).^2;


end
